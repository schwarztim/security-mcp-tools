const execAsync = promisify(exec);

// Configuration
const KALI_HOST = process.env.PROXYCHAINS_KALI_HOST || "kali";
const SSH_TIMEOUT = parseInt(process.env.PROXYCHAINS_SSH_TIMEOUT || "30000", 10);
const CONFIG_DIR = process.env.PROXYCHAINS_CONFIG_DIR || path.join(os.homedir(), ".proxychains-mcp");

// Types
type ChainMode = "strict_chain" | "dynamic_chain" | "random_chain" | "round_robin_chain";
type ProxyType = "http" | "socks4" | "socks5" | "raw";
type DnsMode = "proxy_dns" | "proxy_dns_old" | "proxy_dns_daemon" | "none";

interface Proxy {
  type: ProxyType;
  host: string;
  port: number;
  user?: string;
  pass?: string;
}

interface ProxychainsConfig {
  chainMode: ChainMode;
  dnsMode: DnsMode;
  chainLen?: number;
  tcpReadTimeout?: number;
  tcpConnectTimeout?: number;
  quiet?: boolean;
  proxies: Proxy[];
}

// Default configuration
const defaultConfig: ProxychainsConfig = {
  chainMode: "dynamic_chain",
  dnsMode: "proxy_dns",
  tcpReadTimeout: 15000,
  tcpConnectTimeout: 8000,
  quiet: true,
  proxies: [],
};

// Storage for current configuration
let currentConfig: ProxychainsConfig = { ...defaultConfig };

// Helper functions
async function ensureConfigDir(): Promise<void> {
  try {
    await fs.mkdir(CONFIG_DIR, { recursive: true });
  } catch (error) {
    // Directory might already exist
  }
}

async function loadConfig(): Promise<ProxychainsConfig> {
  try {
    const configPath = path.join(CONFIG_DIR, "config.json");
    const data = await fs.readFile(configPath, "utf-8");
    currentConfig = { ...defaultConfig, ...JSON.parse(data) };
    return currentConfig;
  } catch {
    return currentConfig;
  }
}

async function saveConfig(): Promise<void> {
  await ensureConfigDir();
  const configPath = path.join(CONFIG_DIR, "config.json");
  await fs.writeFile(configPath, JSON.stringify(currentConfig, null, 2));
}

function generateProxychainsConf(): string {
  const lines: string[] = [
    "# ProxyChains-ng configuration generated by proxychains-mcp",
    "",
  ];

  // Chain mode
  lines.push(`${currentConfig.chainMode}`);
  lines.push("");

  // DNS settings
  if (currentConfig.dnsMode !== "none") {
    lines.push(currentConfig.dnsMode);
  }
  lines.push("");

  // Timeouts
  if (currentConfig.tcpReadTimeout) {
    lines.push(`tcp_read_time_out ${currentConfig.tcpReadTimeout}`);
  }
  if (currentConfig.tcpConnectTimeout) {
    lines.push(`tcp_connect_time_out ${currentConfig.tcpConnectTimeout}`);
  }
  lines.push("");

  // Chain length (for random/round_robin modes)
  if (
    currentConfig.chainLen &&
    (currentConfig.chainMode === "random_chain" ||
      currentConfig.chainMode === "round_robin_chain")
  ) {
    lines.push(`chain_len = ${currentConfig.chainLen}`);
    lines.push("");
  }

  // Proxy list
  lines.push("[ProxyList]");
  for (const proxy of currentConfig.proxies) {
    let proxyLine = `${proxy.type}\t${proxy.host}\t${proxy.port}`;
    if (proxy.user) {
      proxyLine += `\t${proxy.user}`;
      if (proxy.pass) {
        proxyLine += `\t${proxy.pass}`;
      }
    }
    lines.push(proxyLine);
  }

  return lines.join("\n");
}

async function executeOnKali(
  command: string,
  timeout: number = SSH_TIMEOUT
): Promise<{ stdout: string; stderr: string }> {
  try {
    const result = await execAsync(`ssh ${KALI_HOST} "${command.replace(/"/g, '\\"')}"`, {
      timeout,
    });
    return result;
  } catch (error: any) {
    throw new McpError(
      ErrorCode.InternalError,
      `SSH command failed: ${error.message}`
    );
  }
}

async function runWithProxychains(
  command: string,
  customConfig?: string
): Promise<{ stdout: string; stderr: string }> {
  let sshCommand: string;

  if (customConfig) {
    // Write temp config and use it
    const tempConfig = `/tmp/proxychains_${Date.now()}.conf`;
    const escapedConfig = customConfig.replace(/'/g, "'\\''");
    sshCommand = `echo '${escapedConfig}' > ${tempConfig} && proxychains4 -q -f ${tempConfig} ${command} ; rm -f ${tempConfig}`;
  } else {
    // Generate config from current settings
    const config = generateProxychainsConf();
    const tempConfig = `/tmp/proxychains_${Date.now()}.conf`;
    const escapedConfig = config.replace(/'/g, "'\\''");
    sshCommand = `echo '${escapedConfig}' > ${tempConfig} && proxychains4 -q -f ${tempConfig} ${command} ; rm -f ${tempConfig}`;
  }

  return executeOnKali(sshCommand, SSH_TIMEOUT * 2);
}

