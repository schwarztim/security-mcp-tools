// Handle tool calls
server.setRequestHandler(CallToolRequestSchema, async (request) => {
  const { name, arguments: args } = request.params;

  try {
    // Load config at start
    await loadConfig();

    switch (name) {
      case "proxychains_run": {
        const { command, timeout } = args as { command: string; timeout?: number };

        if (!command) {
          throw new McpError(ErrorCode.InvalidParams, "Command is required");
        }

        // Check if we have proxies configured
        if (currentConfig.proxies.length === 0) {
          return {
            content: [
              {
                type: "text",
                text: "Error: No proxies configured. Use proxychains_add to add proxies first.",
              },
            ],
          };
        }

        const startTime = Date.now();
        const result = await runWithProxychains(command);
        const elapsed = Date.now() - startTime;

        return {
          content: [
            {
              type: "text",
              text: `Command executed through proxy chain in ${elapsed}ms\n\n--- STDOUT ---\n${result.stdout}\n\n--- STDERR ---\n${result.stderr}`,
            },
          ],
        };
      }

      case "proxychains_config": {
        const { action, config } = args as {
          action: "get" | "set" | "reset";
          config?: Partial<ProxychainsConfig>;
        };

        if (action === "get") {
          return {
            content: [
              {
                type: "text",
                text: `Current configuration:\n${JSON.stringify(currentConfig, null, 2)}\n\nGenerated proxychains.conf:\n${generateProxychainsConf()}`,
              },
            ],
          };
        } else if (action === "set") {
          if (!config) {
            throw new McpError(ErrorCode.InvalidParams, "Config object required for 'set' action");
          }
          currentConfig = { ...currentConfig, ...config };
          await saveConfig();
          return {
            content: [
              {
                type: "text",
                text: `Configuration updated:\n${JSON.stringify(currentConfig, null, 2)}`,
              },
            ],
          };
        } else if (action === "reset") {
          currentConfig = { ...defaultConfig };
          await saveConfig();
          return {
            content: [
              {
                type: "text",
                text: "Configuration reset to defaults",
              },
            ],
          };
        }
        break;
      }

      case "proxychains_add": {
        const { type, host, port, user, pass, position } = args as {
          type: ProxyType;
          host: string;
          port: number;
          user?: string;
          pass?: string;
          position?: number;
        };

        const proxy: Proxy = { type, host, port };
        if (user) proxy.user = user;
        if (pass) proxy.pass = pass;

        if (position !== undefined && position >= 0 && position <= currentConfig.proxies.length) {
          currentConfig.proxies.splice(position, 0, proxy);
        } else {
          currentConfig.proxies.push(proxy);
        }

        await saveConfig();

        return {
          content: [
            {
              type: "text",
              text: `Added proxy: ${type} ${host}:${port}\nTotal proxies: ${currentConfig.proxies.length}`,
            },
          ],
        };
      }

      case "proxychains_remove": {
        const { index, host, port } = args as {
          index?: number;
          host?: string;
          port?: number;
        };

        let removedProxy: Proxy | undefined;

        if (index !== undefined) {
          if (index < 0 || index >= currentConfig.proxies.length) {
            throw new McpError(ErrorCode.InvalidParams, `Invalid index: ${index}`);
          }
          removedProxy = currentConfig.proxies.splice(index, 1)[0];
        } else if (host && port) {
          const idx = currentConfig.proxies.findIndex(
            (p) => p.host === host && p.port === port
          );
          if (idx === -1) {
            throw new McpError(ErrorCode.InvalidParams, `Proxy not found: ${host}:${port}`);
          }
          removedProxy = currentConfig.proxies.splice(idx, 1)[0];
        } else {
          throw new McpError(
            ErrorCode.InvalidParams,
            "Either index or host+port is required"
          );
        }

        await saveConfig();

        return {
          content: [
            {
              type: "text",
              text: `Removed proxy: ${removedProxy.type} ${removedProxy.host}:${removedProxy.port}\nRemaining proxies: ${currentConfig.proxies.length}`,
            },
          ],
        };
      }

      case "proxychains_mode": {
        const { mode, chainLen } = args as { mode: ChainMode; chainLen?: number };

        currentConfig.chainMode = mode;
        if (chainLen !== undefined) {
          currentConfig.chainLen = chainLen;
        }

        await saveConfig();

        return {
          content: [
            {
              type: "text",
              text: `Chain mode set to: ${mode}${chainLen ? ` (chain_len: ${chainLen})` : ""}`,
            },
          ],
        };
      }

      case "proxychains_dns": {
        const { mode } = args as { mode: DnsMode };

        currentConfig.dnsMode = mode;
        await saveConfig();

        return {
          content: [
            {
              type: "text",
              text: `DNS mode set to: ${mode}`,
            },
          ],
        };
      }

      case "proxychains_test": {
        const { target = "https://httpbin.org/ip", verbose = false } = args as {
          target?: string;
          verbose?: boolean;
        };

        if (currentConfig.proxies.length === 0) {
          return {
            content: [
              {
                type: "text",
                text: "Error: No proxies configured. Use proxychains_add to add proxies first.",
              },
            ],
          };
        }

        // Test connectivity
        const startTime = Date.now();
        try {
          const result = await runWithProxychains(`curl -s -o /dev/null -w "%{http_code}|%{time_total}" ${target}`);
          const elapsed = Date.now() - startTime;
          const [httpCode, curlTime] = result.stdout.trim().split("|");

          let response = `Proxy chain test results:\n`;
          response += `- Target: ${target}\n`;
          response += `- HTTP Status: ${httpCode}\n`;
          response += `- Total Time: ${elapsed}ms\n`;
          response += `- Curl Time: ${parseFloat(curlTime) * 1000}ms\n`;
          response += `- Chain Mode: ${currentConfig.chainMode}\n`;
          response += `- Proxies: ${currentConfig.proxies.length}\n`;

          if (verbose) {
            const verboseResult = await runWithProxychains(`curl -s ${target}`);
            response += `\n--- Response Body ---\n${verboseResult.stdout}`;
          }

          return {
            content: [
              {
                type: "text",
                text: response,
              },
            ],
          };
        } catch (error: any) {
          return {
            content: [
              {
                type: "text",
                text: `Proxy chain test FAILED:\n${error.message}\n\nThis could indicate:\n- One or more proxies are down (try dynamic_chain mode)\n- Network connectivity issues\n- Target is blocking proxy connections`,
              },
            ],
          };
        }
      }

      case "proxychains_list": {
        if (currentConfig.proxies.length === 0) {
          return {
            content: [
              {
                type: "text",
                text: "No proxies configured.\n\nUse proxychains_add to add proxies.",
              },
            ],
          };
        }

        let list = `Configured proxies (${currentConfig.proxies.length}):\n`;
        list += `Chain mode: ${currentConfig.chainMode}\n\n`;
        currentConfig.proxies.forEach((proxy, idx) => {
          list += `${idx}: ${proxy.type}\t${proxy.host}:${proxy.port}`;
          if (proxy.user) list += ` (auth: ${proxy.user})`;
          list += "\n";
        });

        return {
          content: [
            {
              type: "text",
              text: list,
            },
          ],
        };
      }

      case "proxychains_import": {
        const { source, format = "auto", defaultType = "socks5" } = args as {
          source: string;
          format?: "auto" | "proxychains" | "ip:port" | "type://ip:port";
          defaultType?: ProxyType;
        };

        let content: string;

        // Fetch content from URL or read from file
        if (source.startsWith("http://") || source.startsWith("https://")) {
          const result = await execAsync(`curl -s "${source}"`);
          content = result.stdout;
        } else {
          // Try to read from Kali
          const result = await executeOnKali(`cat "${source}"`);
          content = result.stdout;
        }

        const lines = content.split("\n").filter((line) => line.trim() && !line.startsWith("#"));
        const imported: Proxy[] = [];

        for (const line of lines) {
          let proxy: Proxy | null = null;

          // Try different formats
          // Format: type://host:port or type://user:pass@host:port
          const urlMatch = line.match(/^(http|socks4|socks5):\/\/(?:([^:]+):([^@]+)@)?([^:]+):(\d+)/);
          if (urlMatch) {
            proxy = {
              type: urlMatch[1] as ProxyType,
              host: urlMatch[4],
              port: parseInt(urlMatch[5], 10),
            };
            if (urlMatch[2]) proxy.user = urlMatch[2];
            if (urlMatch[3]) proxy.pass = urlMatch[3];
          }

          // Format: type host port [user] [pass]
          if (!proxy) {
            const pcMatch = line.match(/^(http|socks4|socks5|raw)\s+(\S+)\s+(\d+)(?:\s+(\S+))?(?:\s+(\S+))?/);
            if (pcMatch) {
              proxy = {
                type: pcMatch[1] as ProxyType,
                host: pcMatch[2],
                port: parseInt(pcMatch[3], 10),
              };
              if (pcMatch[4]) proxy.user = pcMatch[4];
              if (pcMatch[5]) proxy.pass = pcMatch[5];
            }
          }

          // Format: host:port
          if (!proxy) {
            const simpleMatch = line.match(/^(\S+):(\d+)$/);
            if (simpleMatch) {
              proxy = {
                type: defaultType,
                host: simpleMatch[1],
                port: parseInt(simpleMatch[2], 10),
              };
            }
          }

          if (proxy) {
            imported.push(proxy);
          }
        }

        currentConfig.proxies.push(...imported);
        await saveConfig();

        return {
          content: [
            {
              type: "text",
              text: `Imported ${imported.length} proxies from ${source}\nTotal proxies: ${currentConfig.proxies.length}`,
            },
          ],
        };
      }

      case "proxychains_export": {
        const { format = "proxychains" } = args as {
          format?: "proxychains" | "json" | "ip:port";
        };

        let output: string;

        switch (format) {
          case "proxychains":
            output = generateProxychainsConf();
            break;
          case "json":
            output = JSON.stringify(currentConfig, null, 2);
            break;
          case "ip:port":
            output = currentConfig.proxies.map((p) => `${p.host}:${p.port}`).join("\n");
            break;
          default:
            output = generateProxychainsConf();
        }

        return {
          content: [
            {
              type: "text",
              text: output,
            },
          ],
        };
      }

      default:
        throw new McpError(ErrorCode.MethodNotFound, `Unknown tool: ${name}`);
    }
  } catch (error: any) {
    if (error instanceof McpError) {
      throw error;
    }
    throw new McpError(ErrorCode.InternalError, error.message);
  }

  throw new McpError(ErrorCode.InternalError, "Unexpected code path");
});

// Main entry point
async function main() {
  await ensureConfigDir();
  await loadConfig();

  const transport = new StdioServerTransport();
  await server.connect(transport);

  console.error("Proxychains MCP server started");
}

main().catch((error) => {
  console.error("Fatal error:", error);
  process.exit(1);
});
